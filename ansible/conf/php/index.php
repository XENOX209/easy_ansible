<?php
require "GET.php";
require "PUT.php";
require "POST.php";
require "DELETE.php";
require "GET_ID.php";
//call each function
switch ($_SERVER['REQUEST_METHOD']){
	case "GET":
		if($_GET["id"]==NULL)
			mysql_GET();
		else 
			#echo $_GET["id"];
			mysql_GET_ID($_GET["id"]);
		break;
	case "PUT":
		mysql_PUT();
		break;
	case "DELETE":
		#echo $_GET["id"];
		mysql_DELETE($_GET["id"]);
		break;
	case "POST":
		mysql_POST();
		break;
	default:
		echo "error http request method\n";
		echo $_SERVER['REQUEST_METHOD'];

}
?>
